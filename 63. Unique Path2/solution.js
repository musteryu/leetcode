var uniquePathsWithObstacles = function(obstacleGrid) {
    var m = obstacleGrid.length, n = obstacleGrid[0].length,
        paths = new Array(m).fill().map((e, i) => new Array(n).fill(0)),
        i, j;
    for (i = 0; i < n; i++) {
        if (!obstacleGrid[0][i])
            paths[0][i] = 1;
        else break;
    }
    for (j = 0; j < m; j++) {
        if (!obstacleGrid[j][0])
            paths[j][0] = 1;
        else break;
    }
    for (i = 1; i < n; i++) {
        for (j = 1; j < m; j++) {
            if (!obstacleGrid[j][i])
                paths[j][i] = paths[j-1][i] + paths[j][i-1];
        }
    }
    return paths[m-1][n-1];
};

